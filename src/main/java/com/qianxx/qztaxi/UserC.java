package com.qianxx.qztaxi;

public class UserC {

	private Integer userCertificateId;
	private String realName;
	private String identityNo;

	public Integer getUserCertificateId() {
		return userCertificateId;
	}

	public void setUserCertificateId(Integer userCertificateId) {
		this.userCertificateId = userCertificateId;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getIdentityNo() {
		return identityNo;
	}

	public void setIdentityNo(String identityNo) {
		this.identityNo = identityNo;
	}

}